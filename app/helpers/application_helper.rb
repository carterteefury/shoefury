module ApplicationHelper

  def last_product_path
    if session[:last_product_path].nil?
      root_url
    else
      session[:last_product_path]
    end
  end

  def share_text(product)
    brand_name = product.brand ? product.brand.name : ""
    "Found on Karnl: " + brand_name + " " + product.name + " " + number_to_currency(product.price)
  end

  def random_array(products)
    # products.size returns the wrong number for some reason, so counting it the hard way
    count = 0 
    products.each do |p|
      count += 1
    end
    (0..count-1).to_a.shuffle
  end

